#include <iostream>
// using namespace std;
int main(){
    char g = 'A'; // character data type -> 1 byte
    int a = 20; // integer data type -> 4 bytes
    float b = 29.10; // float data type -> 4 byte
    double c = 11.11; // double data type -> 8 byte
    bool f = true; // boolean data type -> 1 byte
    // void empty -> 0 Byte
    std::string name = "swayam"; // string data type for text 
    std::cout<<"Hello World";
    return 0;
}